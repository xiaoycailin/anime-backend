const escapeRegExp = (value: string) =>
  value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");

const phrasePattern = (terms: string[]) =>
  terms
    .map((term) => escapeRegExp(term.trim()).replace(/\s+/g, "\\s+"))
    .filter(Boolean)
    .join("|");

// Unicode-safe boundary, lebih aman buat Indo/English/Japan/China/Korea
const LEFT_EDGE = String.raw`(?:^|[^\p{L}\p{N}_])`;
const RIGHT_EDGE = String.raw`(?=$|[^\p{L}\p{N}_])`;

const makeLoosePattern = (terms: string[]) =>
  new RegExp(`${LEFT_EDGE}(?:${phrasePattern(terms)})${RIGHT_EDGE}`, "iu");

const makeStartPattern = (terms: string[]) =>
  new RegExp(`^\\s*(?:${phrasePattern(terms)})${RIGHT_EDGE}`, "iu");

const makeSmallTalkPattern = (terms: string[]) =>
  new RegExp(
    `^\\s*(?:${phrasePattern(terms)})${RIGHT_EDGE}(?:[\\s!?.,~。！、，…]*.*)?$`,
    "iu",
  );

