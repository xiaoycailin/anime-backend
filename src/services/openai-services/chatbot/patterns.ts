export function isLikelyOffTopic(query: string) {
  const text = query.trim();

  if (!text) return false;

  // Prompt injection tetap diproses terpisah, jangan dianggap small talk.
  if (isPromptInjection(text)) return true;

  // Hard off-topic intent harus menang walau user menyelipkan kata anime.
  // Contoh: "cara bikin website anime" tetap pertanyaan coding, bukan katalog Weebin.
  if (
    makeLoosePattern([
      "coding",
      "ngoding",
      "source code",
      "programming",
      "pemrograman",
      "web",
      "website",
      "bikin web",
      "buat web",
      "frontend",
      "backend",
      "fullstack",
      "database",
      "sql",
      "api",
      "javascript",
      "typescript",
      "python",
      "php",
      "laravel",
      "react",
      "vue",
      "nodejs",
      "css",
      "html",
      "debug",
      "deploy",
      "server",
      "hosting",
      "domain",
      "github",
      "docker",
    ]).test(text)
  ) {
    return true;
  }

  // Kalau ada sinyal anime/donghua/episode, jangan anggap off-topic.
  // Contoh: "anime romance yang bikin galau" jangan kena OFF_TOPIC karena ada kata romance/galau.
  if (hasContentIntent(text)) return false;

  // Small talk seperti "love you", "apa kabar", "makasih" jangan dianggap off-topic.
  if (isSmallTalk(text)) return false;

  return OFF_TOPIC_PATTERN.test(text);
}
